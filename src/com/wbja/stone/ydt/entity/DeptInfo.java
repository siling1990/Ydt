package com.wbja.stone.ydt.entity;

public class DeptInfo {

	private int id;
	private String id_hospital;
	private String name;
	private String desp;
	private String tele;
	private String chengyuantongji;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getId_hospital() {
		return id_hospital;
	}
	public void setId_hospital(String id_hospital) {
		this.id_hospital = id_hospital;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDesp() {
		return desp;
	}
	public void setDesp(String desp) {
		this.desp = desp;
	}
	public String getTele() {
		return tele;
	}
	public void setTele(String tele) {
		this.tele = tele;
	}
	public String getChengyuantongji() {
		return chengyuantongji;
	}
	public void setChengyuantongji(String chengyuantongji) {
		this.chengyuantongji = chengyuantongji;
	}
	
}
